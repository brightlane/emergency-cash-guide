const fs = require('fs');
const path = require('path');

const ROOT = __dirname;
const OUTPUT_DIR = path.join(ROOT, 'output');
const ARTICLES_DIR = path.join(OUTPUT_DIR, 'articles');

function ensureDirs() {
  if (!fs.existsSync(OUTPUT_DIR)) fs.mkdirSync(OUTPUT_DIR, { recursive: true });
  if (!fs.existsSync(ARTICLES_DIR)) fs.mkdirSync(ARTICLES_DIR, { recursive: true });
}

function slugify(text) {
  return text.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');
}

function buildArticleHtml(topic) {
  const title = topic.keyword;
  const slug = slugify(title);
  const now = new Date().toISOString();

  return `<!doctype html>
<html lang="en">
<head>
  <meta charset="utf-8">
  <meta name="viewport" content="width=device-width,initial-scale=1">
  <title>${title} | Emergency Cash Guide</title>
  <meta name="description" content="Auto-generated article about ${title}.">
  //brightlane.github.io/emergency-cash-guide/articles/${slug}.html">
  <meta name="robots" content="index,follow">
</head>
<body>
  <main>
    <header>
      <h1>${title}</h1>
      <p>Generated at ${now}.</p>
    </header>
    <section>
      <p>This article was generated by Claudio’s article engine as part of the nonstop batch pipeline.</p>
      <p>It can be expanded later with SEO sections, FAQs, comparisons, and links.</p>
    </section>
  </main>
</body>
</html>`;
}

function writeArticle(topic) {
  ensureDirs();
  const slug = slugify(topic.keyword);
  const filePath = path.join(ARTICLES_DIR, `${slug}.html`);
  const html = buildArticleHtml(topic);
  fs.writeFileSync(filePath, html, 'utf8');
  return filePath;
}

function main() {
  const topic = process.argv.slice(2).join(' ').trim();
  if (!topic) {
    console.error('Usage: node article-generator.js "topic keyword"');
    process.exit(1);
  }

  const file = writeArticle({ keyword: topic });
  console.log(`generated ${file}`);
}

if (require.main === module) {
  main();
}

module.exports = { writeArticle, buildArticleHtml, slugify };
